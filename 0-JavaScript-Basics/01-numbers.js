// shorthand operators
var y = 5;
console.log("y = " + y);
y = 5 + 1; //6
console.log("y = " + y);
y = y + 1; //7
console.log("y = " + y);

// shorthand operators
var x = 5;
x += 1; // same as saying x = x + 1
console.log("x = " + x); //6

